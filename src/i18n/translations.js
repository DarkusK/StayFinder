// Resolve a dot-path key for a language, with English fallback, and interpolate/execute.
export function translate(lang, key, vars) {
  const dict = translations[lang] || translations.en
  const read = (obj) => key.split('.').reduce((o, k) => (o == null ? undefined : o[k]), obj)
  let value = read(dict)
  if (value === undefined) value = read(translations.en)
  if (typeof value === 'function') return value(vars || {})
  if (typeof value === 'string' && vars) {
    return value.replace(/\{(\w+)\}/g, (_, k) => (vars[k] != null ? vars[k] : `{${k}}`))
  }
  return value !== undefined ? value : key
}
